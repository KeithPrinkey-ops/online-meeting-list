export type Language = keyof typeof languages;

export const enLangs = Object.keys(languages).map(
  lang => languages[lang as Language].english_name
);

export function isLanguage(string: string) {
  return enLangs.includes(string);
}

export function getLanguage(): keyof typeof languages {
  const [lang] = navigator.language.toLowerCase().split('-');
  const useLanguage = lang in languages ? lang : 'en';
  return useLanguage as keyof typeof languages;
}

export function getStrings(language: keyof typeof languages) {
  return languages[language].strings;
}
